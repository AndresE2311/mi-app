  capital numeric,
  tasa_ea numeric,
  fecha_inicio date,
  fecha_vencimiento date,
  valor_compra numeric,
  valor_actual numeric,
  divisa text default 'COP',
  tc_compra numeric,
  tc_actual numeric,
  costos numeric,
  cobrado boolean default false,
