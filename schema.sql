-- ── 2) DEUDAS ──
create table deudas (
  id bigint generated by default as identity primary key,
  user_id uuid not null references auth.users(id) on delete cascade,
  nombre text not null,
  tipo text not null,
  capital numeric not null default 0,
  fecha date not null,
  tipo_tasa text,
  tasa_fija numeric default 0,
  frecuencia text,
  cuotas integer default 0,
  es_tarjeta_auto boolean default false,
  created_at timestamptz default now()
);

-- ── 3) MOVIMIENTOS ──
-- cargo_id se vincula más abajo (dependencia circular con cargos_tarjeta)
create table movimientos (
  id bigint generated by default as identity primary key,
  user_id uuid not null references auth.users(id) on delete cascade,
  descripcion text,
  valor numeric not null default 0,
  tipo text not null,
  fecha date not null,
  categoria text,
  subcategoria text,
  metodo_pago text,
  es_credito boolean default false,
  deuda_id bigint references deudas(id) on delete set null,
  cargo_id bigint,
  created_at timestamptz default now()
);

-- ── 4) CARGOS DE TARJETA ──
create table cargos_tarjeta (
  id bigint generated by default as identity primary key,
  user_id uuid not null references auth.users(id) on delete cascade,
  deuda_id bigint references deudas(id) on delete cascade,
  movimiento_id bigint references movimientos(id) on delete set null,
  fecha date not null,
  descripcion text,
  valor numeric not null default 0,
  pagado boolean default false,
  created_at timestamptz default now()
);

-- Ahora sí, vincular movimientos.cargo_id con cargos_tarjeta
alter table movimientos
  add constraint movimientos_cargo_id_fkey
  foreign key (cargo_id) references cargos_tarjeta(id) on delete set null;

-- ── 5) PAGOS DE DEUDA ──
create table pagos_deuda (
  id bigint generated by default as identity primary key,
  user_id uuid not null references auth.users(id) on delete cascade,
  deuda_id bigint not null references deudas(id) on delete cascade,
  fecha date not null,
  cuota numeric not null default 0,
  capital_pagado numeric default 0,
  interes numeric default 0,
  tasa_aplicada numeric default 0,
  created_at timestamptz default now()
);

-- ── 6) INVERSIONES ──
create table inversiones (
  id bigint generated by default as identity primary key,
  user_id uuid not null references auth.users(id) on delete cascade,
  tipo text not null,
