-- ── 7) LOTES DE INVERSIÓN (compras individuales, costeo PEPS/promedio) ──
create table lotes_inversion (
  id bigint generated by default as identity primary key,
  user_id uuid not null references auth.users(id) on delete cascade,
  inversion_id bigint not null references inversiones(id) on delete cascade,
  fecha date,
  cantidad numeric default 0,
  precio_unidad numeric default 0,
  divisa_activo text default 'COP',
  tc_compra_cop numeric default 0,
  costo_cop_total numeric default 0,
  comision numeric default 0,
  divisa_comision text default 'COP',
  cantidad_restante numeric default 0,
  cerrado boolean default false,
  created_at timestamptz default now()
);

-- ── 8) VENTAS DE INVERSIÓN ──
create table ventas_inversion (
  id bigint generated by default as identity primary key,
  user_id uuid not null references auth.users(id) on delete cascade,
  inversion_id bigint not null references inversiones(id) on delete cascade,
  fecha date,
  cantidad numeric default 0,
  precio_venta numeric default 0,
  divisa_activo text default 'COP',
  tc_venta_cop numeric default 0,
  valor_cop numeric default 0,
  divisa_destino text default 'COP',
  comision numeric default 0,
  divisa_comision text default 'COP',
  impuesto numeric default 0,
  divisa_impuesto text default 'COP',
  metodo_costo text default 'PROM',
  ganancia_activo_cop numeric default 0,
  ganancia_fx_cop numeric default 0,
  created_at timestamptz default now()
);

-- ── 9) DIVIDENDOS / INTERESES ──
create table dividendos (
  id bigint generated by default as identity primary key,
  user_id uuid not null references auth.users(id) on delete cascade,
  inversion_id bigint not null references inversiones(id) on delete cascade,
  fecha date,
  monto numeric default 0,
  divisa text default 'COP',
  tipo text default 'dividendo',
  destino text default 'divisa',
  tc_cop numeric default 0,
  monto_cop numeric default 0,
  impuesto_retenido numeric default 0,
  divisa_impuesto text default 'COP',
  created_at timestamptz default now()
);

-- ── 10) POSICIONES EN DIVISA (saldo vivo por moneda/cripto) ──
create table posiciones_divisa (
  id bigint generated by default as identity primary key,
  user_id uuid not null references auth.users(id) on delete cascade,
  divisa text not null,
  cantidad numeric default 0,
  costo_prom_cop numeric default 0,
  created_at timestamptz default now(),
  unique (user_id, divisa)
);

-- ── 11) HISTORIAL DE TASAS DE CAMBIO ──
create table tasas_cambio (
  id bigint generated by default as identity primary key,
  user_id uuid not null references auth.users(id) on delete cascade,
  divisa text not null,
  tasa_cop numeric not null,
  fecha date not null,
  nota text,
  created_at timestamptz default now()
);

